/** 역명으로 인덱스를 찾는다 — '역' 접미사 유무 모두 허용. 없으면 -1. */
export function findStationIndex(line, direction, name) {
  const stations = STATION_SEQUENCES[line]?.[direction]
  if (!stations || !name) return -1
  const exact = stations.indexOf(name)
  if (exact !== -1) return exact
  const normalized = name.endsWith('역') ? name.slice(0, -1) : name
  return stations.findIndex((s) => s === normalized || s === `${normalized}역`)
}

/** viewStation 다음 역 이름. 노선/역 데이터가 없거나 종점이면 null. */
export function getNextStation(line, direction, viewStation) {
  const stations = STATION_SEQUENCES[line]?.[direction]
  if (!stations) return null
  const idx = findStationIndex(line, direction, viewStation)
  if (idx === -1 || idx >= stations.length - 1) return null
  return stations[idx + 1]
}

/** 해당 노선·방향의 종점(명목상 마지막 역). 데이터 없으면 null. */
export function getTerminalStation(line, direction) {
  const stations = STATION_SEQUENCES[line]?.[direction]
  if (!stations || stations.length === 0) return null
  return stations[stations.length - 1]
}
